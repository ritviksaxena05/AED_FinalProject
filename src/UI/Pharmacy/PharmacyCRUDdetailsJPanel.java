    private void populateMedicineDetails() {
        DefaultTableModel model = (DefaultTableModel) tableMedDetails1.getModel();

        model.setRowCount(0);

        for (Prescription t : ecoModel.getPharmacy().getPrescriptionList()) {
                Object[] row = new Object[4];                
                row[0] = t;
                row[1] = t.getMedUsage();
                row[2] =t.getMedQuantity();
                row[3] = t.getMedPrice();
                model.addRow(row);
        }
    }
    
